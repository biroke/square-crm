package com.square.core.util;

/**
 * Classe utilitaire regroupant les messages pour les ressources.
 * @author Nicolas NADEAU (nicolas.nadeau@scub.net) - SCUB
 */
public class RessourceKeyUtil {

    /** Constructeur par defaut. */
    private RessourceKeyUtil() {
    }

    /** Message d'erreur lorsqu'une ressource n'existe pas. */
    public static final String MESSAGE_ERREUR_RESSOURCE_INEXISTANTE = "message_erreur_ressource_inexistante";

    /** Message d'erreur pour indiquer que le nom d'une ressource est obligatoire. */
    public static final String MESSAGE_ERREUR_RESSOURCE_NOM_OBLIGATOIRE = "message_erreur_ressource_nom_obligatoire";

    /** Message d'erreur pour indiquer que le prénom d'une ressource est obligatoire. */
    public static final String MESSAGE_ERREUR_RESSOURCE_PRENOM_OBLIGATOIRE = "message_erreur_ressource_prenom_obligatoire";

    /** Message d'erreur pour indiquer que l'état d'une ressource est obligatoire. */
    public static final String MESSAGE_ERREUR_RESSOURCE_ETAT_OBLIGATOIRE = "message_erreur_ressource_etat_obligatoire";

    /** Message d'erreur pour indiquer que l'agence d'une ressource est obligatoire. */
    public static final String MESSAGE_ERREUR_RESSOURCE_AGENCE_OBLIGATOIRE = "message_erreur_ressource_agence_obligatoire";

    /** Message d'erreur pour indiquer que le service d'une ressource est obligatoire. */
    public static final String MESSAGE_ERREUR_RESSOURCE_SERVICE_OBLIGATOIRE = "message_erreur_ressource_service_obligatoire";

    /** Message d'erreur pour indiquer que la fonction d'une ressource est obligatoire. */
    public static final String MESSAGE_ERREUR_RESSOURCE_FONCTION_OBLIGATOIRE = "message_erreur_ressource_fonction_obligatoire";

    /** Message d'erreur lorsque l'état d'une ressource n'existe pas. */
    public static final String MESSAGE_ERREUR_RESSOURCE_ETAT_INEXISTANT = "message_erreur_ressource_etat_inexistant";

    /** Message d'erreur lorsque l'agence d'une ressource n'existe pas. */
    public static final String MESSAGE_ERREUR_RESSOURCE_AGENCE_INEXISTANTE = "message_erreur_ressource_agence_inexistante";

    /** Message d'erreur lorsque le service d'une ressource n'existe pas. */
    public static final String MESSAGE_ERREUR_RESSOURCE_SERVICE_INEXISTANT = "message_erreur_ressource_service_inexistant";

    /** Message d'erreur lorsque la fonction d'une ressource n'existe pas. */
    public static final String MESSAGE_ERREUR_RESSOURCE_FONCTION_INEXISTANTE = "message_erreur_ressource_fonction_inexistante";
}
